import Foundation
import SwiftUI

public extension Color {
    public static var primaryColor: Color {
        return Color(UIColor(named: "PrimaryColor")!)
    }
    public static var secondaryColor: Color {
        return Color(UIColor(named: "SecondaryColor")!)
    }
}
